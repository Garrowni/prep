def hello(name):
    print('Hello, ' + name )

hello('Alice')
# def hello(name): --> DEFINES the function
# hello('Alice') --> CALLS the function
# hello('Alice') --> PASSES the string value 'Alice" to the function
# a value passed to a function is called an ARGUMENT the ARGUMENT is assigned to a local variable name
# a variable with an argument assigned to it is a PARAMETER
